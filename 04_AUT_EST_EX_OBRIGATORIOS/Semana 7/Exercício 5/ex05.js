function verificar()
{
    var quant = document.getElementById("qnt").value;
    for (var i = 0; i < quant; i++) 
    {
        var prova = document.createElement("input")
        prova.setAttribute("id", "prova"+i)
        prova.setAttribute("type", "number")
        prova.appendChild.getElementById("div")
        //<input id="prova"type="number"> </input>
    }
}